void GUI_Point(unsigned char X,unsigned char Y,unsigned char draw_mode)
{

    if((X>=192)||(Y>=64))	return;

    u8 PAGE_Y=7-Y/8;		//ÁÐ»»Ëã³ÉPAGE
    u8 char_Y=Y%8;			//Ëã³öÒª²Ù×÷µÄÎ»

    switch (draw_mode%=3)
    {
    case 0:
        Disp_buf[PAGE_Y*192+X] = Disp_buf[PAGE_Y*192+X]&=~(1<<(7-char_Y));
        //DSP_BUF[PAGE_Y][X]&=~_BV(7-char_Y);
        break;
    case 1:
        Disp_buf[PAGE_Y*192+X] = Disp_buf[PAGE_Y*192+X]|=1<<(7-char_Y);
        //DSP_BUF[PAGE_Y][X]|=_BV(7-char_Y);
        break;
    case 2:
        Disp_buf[PAGE_Y*192+X] = Disp_buf[PAGE_Y*192+X]^=1<<(7-char_Y);
        //DSP_BUF[PAGE_Y][X]^=_BV(7-char_Y);
        break;

    default:
        break;
    }
}

/***********************************************************************
»­Ò»Ìõx1,y1µ½x2,y2µÄÖ±Ïß
(show=1»­µã£¬show=0Çåµã)
***********************************************************************/
void glcd_line(int x1, int y1, int x2, int y2, unsigned char show)
{
    int dy ;
    int dx ;
    int stepx, stepy, fraction;
    dy = y2 - y1;
    dx = x2 - x1;
    if (dy < 0)
    {
        dy = -dy;
        stepy = -1;
    }
    else
    {
        stepy = 1;
    }
    if (dx < 0)
    {
        dx = -dx;
        stepx = -1;
    }
    else
    {
        stepx = 1;
    }
    dy <<= 1;
    dx <<= 1;
    GUI_Point(x1,y1,show);
    if (dx > dy)
    {
        fraction = dy - (dx >> 1);
        while (x1 != x2)
        {
            if (fraction >= 0)
            {
                y1 += stepy;
                fraction -= dx;
            }
            x1 += stepx;
            fraction += dy;
            GUI_Point(x1,y1,show);
        }
    }
    else
    {
        fraction = dx - (dy >> 1);
        while (y1 != y2)
        {
            if (fraction >= 0)
            {
                x1 += stepx;
                fraction -= dy;
            }
            y1 += stepy;
            fraction += dx;
            GUI_Point(x1,y1,show);
        }
    }
}

/***********************************************************************
ÒÔx,yÎªÔ²ÐÄ£¬ÒÔradiusÎª°ë¾¶»­ºØ
(show=1»­µã£¬show=0Çåµã)
***********************************************************************/
void glcd_circle(int x, int y, int radius, unsigned char show)
{
    int xc = 0;
    int yc ;
    int p ;
    yc=radius;
    p = 3 - (radius<<1);
    while (xc <= yc)
    {
        GUI_Point(x + xc, y + yc, show);
        GUI_Point(x + xc, y - yc, show);
        GUI_Point(x - xc, y + yc, show);
        GUI_Point(x - xc, y - yc, show);
        GUI_Point(x + yc, y + xc, show);
        GUI_Point(x + yc, y - xc, show);
        GUI_Point(x - yc, y + xc, show);
        GUI_Point(x - yc, y - xc, show);
        if (p < 0)
            p += (xc++ << 2) + 6;
        else
            p += ((xc++ - yc--)<<2) + 10;
    }
}




